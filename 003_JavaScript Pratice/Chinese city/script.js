const provinceSelect = document.getElementById('provinceSelect');
const output = document.getElementById('output');

Object.keys(chinaProvinces).forEach(province => {
    const option = document.createElement('option');
    option.value = province;
    option.textContent = province;
    provinceSelect.appendChild(option);
});
//Object.keys() 参考⇒https://developer.mozilla.org/zh-CN/docs/Web/JavaScript/Reference/Global_Objects/Object/keys
//forEach() 参考⇒https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Array/forEach
//createElement() 参考⇒https://www.w3schools.com/jsref/met_document_createelement.asp

provinceSelect.addEventListener('change', () => {
    const selectedProvince = provinceSelect.value;
    const data = chinaProvinces[selectedProvince];

    if (data) {
        output.innerHTML = `<strong>省会:</strong> ${data["省会"]}\n\n<strong>城市:</strong>\n${data["城市"].join('\n')}`;
    } else {
        output.innerHTML = "请选择一个省份";
    }
});